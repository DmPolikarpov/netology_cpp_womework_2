#include <cmath>

#include "math61.h"

double sum(double val1, double val2) { return val1 + val2; };
double difference(double val1, double val2) { return val1 - val2; };
double multiplication(double val1, double val2) { return val1 * val2; };
double division(double val1, double val2) { return val1 / val2; };
double power(double val1, double val2) { return pow(val1, val2); };